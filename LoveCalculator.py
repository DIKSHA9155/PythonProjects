def calculate_love_score(name1, name2):
    count_name1 = 0
    count_name2 = 0
    a = ["T","R","U","E","L","O","V","E"]
    for i in a:
        for j in name1.upper():
            if i==j:
                count_name1+=1
        for k in name2.upper():
            if i==k:
                count_name2+=1
    print(str(count_name1) + str(count_name2))

